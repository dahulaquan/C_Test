void test3(char board[ROW][COL], int row, int col)
{
	int x = 0;
	int y = 0;
	printf("请输入：");
	while (1)
	{
		scanf("%d%d", &x, &y);
		if (x >= 1 && x <= row && y >= 1 && y <= col)
		{
			if (board[x - 1][y - 1] == ' ')
			{
				board[x - 1][y - 1] = '*';
				break;
			}
			else
			{
				printf("已经重复输入\n");	
			}
		}
		else
		{
			printf("已超出输入范围\n");
		}
	}
}

